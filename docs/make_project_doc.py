"""Render ``docs/مشروع_تحقيق_الحديث.pdf`` — a detailed Arabic project documentary.

A reproducible source for the full project document (intro · the seven layers · علم الرجال in depth ·
the methodology of trust · the sources · the self-audits · the limits). Regenerate after a structural
change so the document never drifts::

    python docs/make_project_doc.py

Requirements (present in CI / the dev box): fpdf2 + uharfbuzz (HarfBuzz Arabic shaping) and the font
Noto Naskh Arabic. The text is pure Arabic prose (فصحى); domain terms stay Arabic. RTL throughout.
"""

from __future__ import annotations

import re
from pathlib import Path

from fpdf import FPDF

# Noto Naskh carries Arabic letters/digits + «: ، ؛ ؟ «»» only — NOT Latin letters, ASCII parens/dashes/
# slash/plus/ellipsis/arrow. Any such glyph renders as tofu and breaks the RTL line, so the document is
# kept PURE Arabic: strip Latin runs and map the ASCII symbols to Arabic punctuation before drawing.
_LATIN = re.compile(r"\(?[A-Za-z][A-Za-z .]*\)?")


def _clean(s: str) -> str:
    s = _LATIN.sub("", s)
    for a, b in (("—", "،"), ("…", ""), ("+", " و"), ("←", " إلى "), ("/", " أو "), ("(", ""), (")", "")):
        s = s.replace(a, b)
    s = re.sub(r"\s+([،؛:.])", r"\1", s)        # no space before Arabic punctuation
    s = re.sub(r"،\s*،", "،", s)                 # collapse a doubled «،» left by a removed run
    return re.sub(r" {2,}", " ", s).strip(" ،")

HERE = Path(__file__).resolve().parent
NASKH = "/usr/share/fonts/truetype/noto/NotoNaskhArabic-Regular.ttf"
NASKH_B = "/usr/share/fonts/truetype/noto/NotoNaskhArabic-Bold.ttf"
OUT = HERE / "مشروع_تحقيق_الحديث.pdf"

INK = (43, 38, 32)          # #2b2620
GREEN = (31, 122, 82)       # #1f7a52
GOLD = (168, 125, 39)       # #a87d27
MUTED = (103, 93, 69)       # #675d45
LINE = (210, 198, 170)
CREAM = (250, 247, 240)


class Doc(FPDF):
    def __init__(self) -> None:
        super().__init__(format="A4")
        self.add_font("n", "", NASKH)
        self.add_font("n", "B", NASKH_B)
        self.set_text_shaping(True)
        self.set_margins(20, 22, 20)
        self.set_auto_page_break(True, margin=20)
        self._cover = True

    def footer(self) -> None:
        if self.page_no() == 1:
            return
        self.set_y(-14)
        self.set_font("n", "", 9)
        self.set_text_color(*MUTED)
        self.cell(0, 8, str(self.page_no()), align="C")


def R(pdf: Doc, txt: str, h: float = 7.2, size: float = 12.5, font: str = "",
      color=INK, gap: float = 1.3) -> None:
    """A right-aligned (RTL) Arabic paragraph."""
    pdf.set_font("n", font, size)
    pdf.set_text_color(*color)
    pdf.multi_cell(0, h, _clean(txt), align="R")
    pdf.ln(gap)


def H1(pdf: Doc, num: str, txt: str) -> None:
    if pdf.get_y() > 244:
        pdf.add_page()
    pdf.ln(2)
    y = pdf.get_y()
    pdf.set_fill_color(*GREEN)
    pdf.rect(pdf.w - 20 - 3.2, y + 0.5, 3.2, 9, "F")           # a green tab on the RIGHT (RTL)
    pdf.set_font("n", "B", 16.5)
    pdf.set_text_color(*GREEN)
    pdf.set_x(20)
    pdf.cell(pdf.w - 40 - 6, 10, f"{_clean(txt)}   {num}", align="R")
    pdf.ln(10.5)


def H2(pdf: Doc, txt: str) -> None:
    if pdf.get_y() > 250:
        pdf.add_page()
    pdf.ln(1)
    R(pdf, txt, h=7.5, size=13.5, font="B", color=GOLD, gap=1.2)


def UL(pdf: Doc, items: list[str]) -> None:
    pdf.set_font("n", "", 12)
    pdf.set_text_color(*INK)
    for it in items:
        pdf.multi_cell(0, 7.0, f"{_clean(it)}  ◂", align="R")   # RTL bullet on the right
        pdf.ln(0.6)
    pdf.ln(1.4)


def CALLOUT(pdf: Doc, txt: str) -> None:
    pdf.ln(1)
    x0, y0 = 20, pdf.get_y()
    pdf.set_font("n", "B", 12)
    pdf.set_text_color(*GREEN)
    pdf.set_fill_color(*CREAM)
    pdf.set_draw_color(*GREEN)
    pdf.set_line_width(0.5)
    pdf.multi_cell(0, 7.4, _clean(txt), align="R", border=1, fill=True, padding=4)
    pdf.ln(2.5)


