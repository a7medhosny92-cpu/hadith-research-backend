def _layer_box(pdf: Doc, x: float, y: float, w: float, num: str, name: str, gloss: str) -> None:
    h = 12.6
    pdf.set_draw_color(*GREEN)
    pdf.set_line_width(0.4)
    pdf.set_fill_color(*CREAM)
    pdf.rect(x, y, w, h, "DF", round_corners=True, corner_radius=2.0)
    # a gold step-badge on the RIGHT (RTL reading order)
    pdf.set_fill_color(*GOLD)
    pdf.rect(x + w - 11, y, 11, h, "F", round_corners=True, corner_radius=2.0)
    pdf.set_font("n", "B", 12)
    pdf.set_text_color(255, 255, 255)
    pdf.set_xy(x + w - 11, y)
    pdf.cell(11, h, num, align="C")
    pdf.set_xy(x + 3, y + 1.6)
    pdf.set_font("n", "B", 12.5)
    pdf.set_text_color(*GREEN)
    pdf.cell(w - 15, 4.8, name, align="R")
    pdf.set_xy(x + 3, y + 6.6)
    pdf.set_font("n", "", 10)
    pdf.set_text_color(*MUTED)
    pdf.cell(w - 15, 4.6, gloss, align="R")


def diagram(pdf: Doc) -> None:
    """A compact, pure-Arabic «seven layers» flow on the cover (replaces the old screenshot)."""
    w = 124.0
    x = (pdf.w - w) / 2
    y = pdf.get_y()
    for i, (num, name, gloss) in enumerate(_LAYERS):
        _layer_box(pdf, x, y, w, num, name, gloss)
        y += 12.6
        if i < len(_LAYERS) - 1:                       # a downward connector between layers
            pdf.set_draw_color(*GOLD)
            pdf.set_line_width(0.6)
            pdf.line(pdf.w / 2, y, pdf.w / 2, y + 2.6)
            y += 2.6
    pdf.set_y(y)


